    private static readonly object locker = new();
    private static Dictionary<char, char>? CHS_to_CHT_dict;
    private static Dictionary<char, char>? CHT_to_CHS_dict;

    public static string ConvertToSimplifiedChinese(string? str)
    {
        return EnsureDictionary() ? ConvertByDictionary(str, CHT_to_CHS_dict!) : throw new ArgumentException();
    }

    public static string ConvertToTraditionalChinese(string? str)
    {
        return EnsureDictionary() ? ConvertByDictionary(str, CHS_to_CHT_dict!) : throw new ArgumentException();
    }

    private static string ConvertByDictionary(string? str, Dictionary<char, char> dict)
    {
        if (string.IsNullOrEmpty(str)) return string.Empty;

        var info = StringInfo.ParseCombiningCharacters(str);
        if (info.Length < 2)
        {
            if (str.Length == info.Length && dict.TryGetValue(str[0], out var resChar))
            {
                return $"{resChar}";
            }
            return str;
        }

        var sb = new StringBuilder(str.Length);

        for (int i = 0; i < info.Length; i++)
        {
            var startIdx = info[i];
            int length;

            if (i != info.Length - 1)
            {
                length = info[i + 1] - startIdx;
            }
            else
            {
                length = str.Length - startIdx;
            }

            if (length == 1)
            {
                var oriChar = str[startIdx];
                if (dict.TryGetValue(oriChar, out var resChar))
                {
                    sb.Append(resChar);
                }
                else
                {
                    sb.Append(oriChar);
                }
            }
            else
            {
                sb.Append(str.AsSpan(startIdx, length));
            }
        }

        return sb.ToString();
    }

    private static bool EnsureDictionary()
    {
        if (CHS_to_CHT_dict == null || CHT_to_CHS_dict == null)
        {
            lock (locker)
            {
                if (CHS_to_CHT_dict == null || CHT_to_CHS_dict == null)
                {
                    CHS_to_CHT_dict = new Dictionary<char, char>();
                    CHT_to_CHS_dict = new Dictionary<char, char>();

                    for (int i = 0; i < CHSWord.Length; i++)
                    {
                        CHS_to_CHT_dict.TryAdd(CHSWord[i], CHTWord[i]);
                        CHT_to_CHS_dict.TryAdd(CHTWord[i], CHSWord[i]);
                    }
                }
            }
        }

        return true;
    }
